import { execFileSync } from 'node:child_process';
import { createHash } from 'node:crypto';
import { existsSync, mkdirSync, readFileSync, readdirSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import matter from 'gray-matter';
import ts from 'typescript';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outputDir = path.join(root, 'docs', 'reference');
const marker = '<!-- Generated by scripts/generate-documentation-reference.mjs; do not edit. -->';
const repositorySourceUrl = 'https://github.com/flytohub/flyto-blog/blob/main';
const checkMode = process.argv.includes('--check');
const sourceExtensions = new Set(['.cjs', '.js', '.jsx', '.mjs', '.mts', '.ts', '.tsx', '.vue']);
const maxRowsPerPage = 220;

function repositoryFiles() {
  return execFileSync('git', ['-C', root, 'ls-files', '--cached', '--others', '--exclude-standard'], { encoding: 'utf8' })
    .split(/\r?\n/)
    .filter(Boolean)
    .sort();
}

function read(relativePath) {
  return readFileSync(path.join(root, relativePath), 'utf8');
}

function parseSource(relativePath) {
  const raw = read(relativePath);
  if (!relativePath.endsWith('.vue')) return { text: raw, lineOffset: 0 };
  const match = raw.match(/<script\b[^>]*>([\s\S]*?)<\/script>/i);
  if (!match) return { text: '', lineOffset: 0 };
  return {
    text: match[1],
    lineOffset: raw.slice(0, match.index + match[0].indexOf(match[1])).split(/\r?\n/).length - 1,
  };
}

function scriptKind(relativePath) {
  if (/\.(?:tsx|jsx)$/.test(relativePath)) return ts.ScriptKind.TSX;
  if (/\.(?:ts|mts)$/.test(relativePath) || relativePath.endsWith('.vue')) return ts.ScriptKind.TS;
  return ts.ScriptKind.JS;
}

function declarationName(node, sourceFile) {
  if (node.name?.getText) return node.name.getText(sourceFile);
  return 'default';
}

function firstComment(node) {
  return ts.getJSDocCommentsAndTags(node)
    .map((entry) => entry.comment)
    .filter((value) => typeof value === 'string')
    .map((value) => value.replace(/\s+/g, ' ').trim())
    .find(Boolean) ?? '';
}

function humanize(value) {
  return String(value)
    .replace(/^use(?=[A-Z])/, '')
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .toLowerCase();
}

function responsibility(kind, name, comment) {
  if (comment) return comment;
  const subject = humanize(name.replace(/^.*\./, '')) || 'source contract';
  if (kind === 'component') return `Renders the ${subject} interface.`;
  if (kind === 'interface' || kind === 'type' || kind === 'enum') return `Defines the ${subject} data contract.`;
  if (kind === 'class') return `Implements the ${subject} behavior.`;
  if (kind === 'constructor') return `Initializes the ${humanize(name.replace(/\.constructor$/, ''))} instance.`;
  if (kind === 'method contract') return `Defines the callable ${subject} contract.`;
  if (kind === 'constant') return `Defines the ${subject} source-of-truth value.`;
  if (/^(read|load|list|find|get|resolve|search|collect)/i.test(name)) return `Retrieves ${subject}.`;
  if (/^(build|create|generate|render|write|make)/i.test(name)) return `Builds ${subject}.`;
  if (/^(check|audit|validate|verify|assert|is|has)/i.test(name)) return `Validates ${subject}.`;
  if (/^(parse|normalize|format|decode|encode|clean|strip)/i.test(name)) return `Transforms ${subject}.`;
  return `Implements ${subject}.`;
}

function signature(node, sourceFile, name) {
  if (ts.isFunctionDeclaration(node) || ts.isMethodDeclaration(node) || ts.isMethodSignature(node)
    || ts.isConstructorDeclaration(node) || ts.isGetAccessorDeclaration(node) || ts.isSetAccessorDeclaration(node)) {
    return `${name}(${node.parameters.map((param) => param.name.getText(sourceFile)).join(', ')})`;
  }
  if ((ts.isVariableDeclaration(node) || ts.isPropertyDeclaration(node)) && node.initializer
    && (ts.isArrowFunction(node.initializer) || ts.isFunctionExpression(node.initializer))) {
    return `${name}(${node.initializer.parameters.map((param) => param.name.getText(sourceFile)).join(', ')})`;
  }
  if (ts.isPropertySignature(node) && node.type && ts.isFunctionTypeNode(node.type)) {
    return `${name}(${node.type.parameters.map((param) => param.name.getText(sourceFile)).join(', ')})`;
  }
  return name;
}

function isTopLevelVariable(node) {
  return ts.isVariableDeclaration(node)
    && ts.isVariableDeclarationList(node.parent)
    && ts.isVariableStatement(node.parent.parent)
    && ts.isSourceFile(node.parent.parent.parent);
}

function declarationsFor(relativePath) {
  const parsed = parseSource(relativePath);
  if (!parsed.text.trim()) return [];
  const sourceFile = ts.createSourceFile(relativePath, parsed.text, ts.ScriptTarget.Latest, true, scriptKind(relativePath));
  const declarations = [];

  function add(node, kind, forcedName = '') {
    const name = forcedName || declarationName(node, sourceFile);
    declarations.push({
      path: relativePath,
      line: sourceFile.getLineAndCharacterOfPosition(node.getStart(sourceFile)).line + 1 + parsed.lineOffset,
      kind,
      name,
      signature: signature(node, sourceFile, name),
      responsibility: responsibility(kind, name, firstComment(node)),
    });
  }

  function visit(node, owner = '') {
    let childOwner = owner;
    if (ts.isFunctionDeclaration(node)) {
      const name = declarationName(node, sourceFile);
      add(node, /^[A-Z]/.test(name) && /\.(?:tsx|jsx|vue)$/.test(relativePath) ? 'component' : 'function', name);
    } else if (ts.isClassDeclaration(node)) {
      add(node, 'class');
      childOwner = declarationName(node, sourceFile);
    } else if (ts.isInterfaceDeclaration(node)) {
      add(node, 'interface');
      childOwner = declarationName(node, sourceFile);
    } else if (ts.isTypeAliasDeclaration(node)) add(node, 'type');
    else if (ts.isEnumDeclaration(node)) add(node, 'enum');
    else if (ts.isConstructorDeclaration(node)) add(node, 'constructor', owner ? `${owner}.constructor` : 'constructor');
    else if (ts.isMethodDeclaration(node)) {
      const name = declarationName(node, sourceFile);
      add(node, 'method', owner ? `${owner}.${name}` : name);
    } else if (ts.isGetAccessorDeclaration(node) || ts.isSetAccessorDeclaration(node)) {
      const name = declarationName(node, sourceFile);
      add(node, ts.isGetAccessorDeclaration(node) ? 'getter' : 'setter', owner ? `${owner}.${name}` : name);
    } else if (ts.isMethodSignature(node)) {
      const name = declarationName(node, sourceFile);
      add(node, 'method contract', owner ? `${owner}.${name}` : name);
    } else if (ts.isPropertyDeclaration(node) && node.initializer
      && (ts.isArrowFunction(node.initializer) || ts.isFunctionExpression(node.initializer))) {
      const name = declarationName(node, sourceFile);
      add(node, 'method', owner ? `${owner}.${name}` : name);
    } else if (ts.isPropertySignature(node) && node.type && ts.isFunctionTypeNode(node.type)) {
      const name = declarationName(node, sourceFile);
      add(node, 'method contract', owner ? `${owner}.${name}` : name);
    } else if (ts.isVariableDeclaration(node)) {
      const name = declarationName(node, sourceFile);
      const callable = node.initializer && (ts.isArrowFunction(node.initializer) || ts.isFunctionExpression(node.initializer));
      if (callable || isTopLevelVariable(node)) {
        add(node, callable && /^[A-Z]/.test(name) && /\.(?:tsx|jsx|vue)$/.test(relativePath) ? 'component' : callable ? 'function' : 'constant', name);
      }
    } else if (ts.isExportAssignment(node)) {
      add(node, 'constant', 'default export');
    } else if (ts.isBinaryExpression(node) && node.operatorToken.kind === ts.SyntaxKind.EqualsToken
      && ts.isExpressionStatement(node.parent) && ts.isSourceFile(node.parent.parent)) {
      add(node, 'constant', node.left.getText(sourceFile));
    }
    ts.forEachChild(node, (child) => visit(child, childOwner));
  }

  visit(sourceFile);
  return declarations;
}

function sourceArea(relativePath) {
  if (relativePath.startsWith('scripts/')) return 'scripts';
  if (relativePath.startsWith('.vitepress/theme/')) return 'theme';
  if (relativePath.startsWith('.vitepress/')) return 'vitepress';
  return 'runtime';
}

function repositoryLink(relativePath, line = 0) {
  const encoded = relativePath.split('/').map(encodeURIComponent).join('/');
  const label = line ? `${relativePath}:${line}` : relativePath;
  const fragment = line ? `#L${line}` : '';
  return `[${label}](${repositorySourceUrl}/${encoded}${fragment})`;
}

function escapeCell(value) {
  return String(value ?? '').replace(/\|/g, '\\|').replace(/\s+/g, ' ').trim();
}

function titleCase(value) {
  return value.replace(/(^|[-_ ])([a-z])/g, (_, prefix, letter) => `${prefix}${letter.toUpperCase()}`);
}

function isoDate(value) {
  if (value instanceof Date && !Number.isNaN(value.valueOf())) return value.toISOString().slice(0, 10);
  return String(value ?? '');
}

function wordCount(value) {
  return (String(value).match(/[A-Za-z0-9]+(?:[-'][A-Za-z0-9]+)*/g) ?? []).length;
}

function postInventory(files) {
  return files.filter((file) => file.startsWith('posts/') && file.endsWith('.md')).map((file) => {
    const parsed = matter(read(file));
    const slug = path.basename(file, '.md');
    const links = [...parsed.content.matchAll(/(?<!!)\[[^\]]+\]\(([^)\s]+)(?:\s+['"][^'"]+['"])?\)/g)].map((match) => match[1]);
    return {
      file,
      slug,
      route: `/posts/${slug}`,
      title: String(parsed.data.title ?? ''),
      description: String(parsed.data.description ?? ''),
      date: isoDate(parsed.data.date),
      author: String(parsed.data.author ?? ''),
      tags: Array.isArray(parsed.data.tags) ? parsed.data.tags.map(String) : [],
      cover: String(parsed.data.cover ?? ''),
      focusKeyword: String(parsed.data.focusKeyword ?? parsed.data.title ?? ''),
      relatedKeywords: Array.isArray(parsed.data.relatedKeywords) ? parsed.data.relatedKeywords.map(String) : [],
      words: wordCount(parsed.content),
      h2: (parsed.content.match(/^##\s+/gm) ?? []).length,
      internalLinks: links.filter((link) => link.startsWith('/')).length,
      externalLinks: links.filter((link) => /^https?:\/\//.test(link)).length,
    };
  }).sort((a, b) => a.slug.localeCompare(b.slug));
}

function workflowInventory(files) {
  return files.filter((file) => /^\.github\/workflows\/.*\.ya?ml$/.test(file)).map((file) => {
    const content = read(file);
    return {
      file,
      name: content.match(/^name:\s*(.+)$/m)?.[1]?.trim() ?? path.basename(file),
      scheduled: /^\s*schedule:/m.test(content),
      manual: /^\s*workflow_dispatch:/m.test(content),
    };
  });
}

function environmentInventory(files) {
  const values = new Map();
  function add(name, source, scope) {
    if (!values.has(name)) values.set(name, { name, scope: new Set(), sources: new Set() });
    values.get(name).scope.add(scope);
    values.get(name).sources.add(source);
  }
  for (const file of files.filter((entry) => sourceExtensions.has(path.extname(entry)))) {
    for (const match of read(file).matchAll(/process\.env\.([A-Z][A-Z0-9_]*)/g)) add(match[1], file, 'runtime');
  }
  for (const file of files.filter((entry) => /^\.github\/workflows\/.*\.ya?ml$/.test(entry))) {
    const content = read(file);
    for (const match of content.matchAll(/secrets\.([A-Z][A-Z0-9_]*)/g)) add(match[1], file, 'GitHub secret');
    for (const match of content.matchAll(/vars\.([A-Z][A-Z0-9_]*)/g)) add(match[1], file, 'GitHub variable');
  }
  if (files.includes('.env.example')) {
    for (const line of read('.env.example').split(/\r?\n/)) {
      const match = line.match(/^([A-Z][A-Z0-9_]*)=/);
      if (match) add(match[1], '.env.example', 'runtime');
    }
  }
  return [...values.values()].map((item) => ({
    name: item.name,
    scope: [...item.scope].sort(),
    sources: [...item.sources].sort(),
  })).sort((a, b) => a.name.localeCompare(b.name));
}

function renderSourcePage(area, pageNumber, pageCount, rows) {
  const title = `${titleCase(area)} Source Reference${pageCount > 1 ? ` ${pageNumber}` : ''}`;
  const lines = [marker, '', `# ${title}`, '', `Source-backed contracts for **${rows.length} declarations**.`, '', '| Kind | Declaration | Responsibility | Source |', '|---|---|---|---|'];
  for (const row of rows) lines.push(`| ${row.kind} | \`${escapeCell(row.signature)}\` | ${escapeCell(row.responsibility)} | ${repositoryLink(row.path, row.line)} |`);
  return `${lines.join('\n')}\n`;
}

function buildOutputs(files) {
  const sourceFiles = files.filter((file) => sourceExtensions.has(path.extname(file)) && !file.endsWith('.d.ts'));
  const declarations = sourceFiles.flatMap(declarationsFor).sort((a, b) => a.path.localeCompare(b.path) || a.line - b.line || a.name.localeCompare(b.name));
  const posts = postInventory(files);
  const workflows = workflowInventory(files);
  const environment = environmentInventory(files);
  const packageJson = JSON.parse(read('package.json'));
  const publicFiles = files.filter((file) => file.startsWith('public/'));
  const socialPlans = files.filter((file) => /^social\/posts\/.*\.json$/.test(file));
  const videoPlans = files.filter((file) => /^video\/plans\/.*\.json$/.test(file));
  const templateCatalog = JSON.parse(read('video/templates/catalog.json'));
  const stockCatalog = JSON.parse(read('video/assets/stock-sources.json'));
  const outputs = new Map();
  const sourcePages = [];

  for (const area of ['vitepress', 'theme', 'scripts', 'runtime']) {
    const rows = declarations.filter((entry) => sourceArea(entry.path) === area);
    if (!rows.length) continue;
    const chunks = Array.from({ length: Math.ceil(rows.length / maxRowsPerPage) }, (_, index) => rows.slice(index * maxRowsPerPage, (index + 1) * maxRowsPerPage));
    chunks.forEach((chunk, index) => {
      const file = `source-${area}-${String(index + 1).padStart(2, '0')}.md`;
      outputs.set(file, renderSourcePage(area, index + 1, chunks.length, chunk));
      sourcePages.push({ area, file, declarations: chunk.length });
    });
  }

  const postChunks = Array.from({ length: Math.ceil(posts.length / 35) }, (_, index) => posts.slice(index * 35, (index + 1) * 35));
  postChunks.forEach((chunk, index) => {
    const lines = [marker, '', `# Editorial Content Reference ${index + 1}`, '', `Frontmatter and content signals for **${chunk.length} published posts**. Canonical URLs are derived from the stable \`posts/<slug>.md\` path.`, '', '| Post | Date | Focus keyword | Tags | Words | H2 | Links (internal / external) | Cover |', '|---|---|---|---|---:|---:|---:|---|'];
    for (const post of chunk) {
      lines.push(`| [${escapeCell(post.title)}](../../${post.file}) | ${post.date} | ${escapeCell(post.focusKeyword)} | ${escapeCell(post.tags.join(', '))} | ${post.words} | ${post.h2} | ${post.internalLinks} / ${post.externalLinks} | \`${escapeCell(post.cover)}\` |`);
    }
    outputs.set(`posts-${String(index + 1).padStart(2, '0')}.md`, `${lines.join('\n')}\n`);
  });

  const automation = [marker, '', '# Automation And Configuration Reference', '', `The repository exposes **${Object.keys(packageJson.scripts ?? {}).length} npm scripts**, **${workflows.length} GitHub workflows**, and **${environment.length} named environment inputs**.`, '', '## NPM Scripts', '', '| Script | Command |', '|---|---|'];
  for (const [name, command] of Object.entries(packageJson.scripts ?? {}).sort(([a], [b]) => a.localeCompare(b))) automation.push(`| \`${name}\` | \`${escapeCell(command)}\` |`);
  automation.push('', '## GitHub Workflows', '', '| Workflow | Scheduled | Manual | Source |', '|---|---|---|---|');
  for (const workflow of workflows) automation.push(`| ${escapeCell(workflow.name)} | ${workflow.scheduled ? 'yes' : 'no'} | ${workflow.manual ? 'yes' : 'no'} | ${repositoryLink(workflow.file)} |`);
  automation.push('', '## Environment Inputs', '', '| Name | Scope | Sources |', '|---|---|---|');
  for (const item of environment) automation.push(`| \`${item.name}\` | ${item.scope.join(', ')} | ${item.sources.map((source) => `\`${source}\``).join(', ')} |`);
  outputs.set('automation.md', `${automation.join('\n')}\n`);

  const distribution = [marker, '', '# Social And Video Distribution Reference', '', 'Distribution is review-first: plans and render contracts are public; credentials and live publishing authority are not.', '', '## Social Plans', '', '| Plan | Channels | Canonical URL |', '|---|---|---|'];
  for (const file of socialPlans) {
    const plan = JSON.parse(read(file));
    distribution.push(`| ${repositoryLink(file)} | ${escapeCell(Object.keys(plan.channels ?? {}).join(', '))} | ${escapeCell(plan.canonicalUrl ?? plan.sourceUrl ?? '')} |`);
  }
  distribution.push('', '## Video Plans', '', '| Plan | Scenes | Outputs | Source URL |', '|---|---:|---:|---|');
  for (const file of videoPlans) {
    const plan = JSON.parse(read(file));
    distribution.push(`| ${repositoryLink(file)} | ${(plan.scenes ?? []).length} | ${(plan.outputs ?? []).length} | ${escapeCell(plan.sourceUrl ?? '')} |`);
  }
  distribution.push('', '## Reusable Assets', '', `- ${templateCatalog.templates?.length ?? 0} repository-owned scene templates across ${Object.keys(templateCatalog.packs ?? {}).length} packs.`, `- ${stockCatalog.assets?.length ?? 0} checksum-pinned licensed stock source entries.`, '- One owned Flyto2 logo is committed for deterministic local and CI rendering.', '- Generated review media stays under ignored `video/dist/`; upload credentials are outside this repository.');
  outputs.set('distribution.md', `${distribution.join('\n')}\n`);

  const discovery = [marker, '', '# Public Discovery And Asset Reference', '', `The public tree contains **${publicFiles.length} tracked files**. Generated feeds and discovery manifests are release artifacts; article covers, diagrams, logo, favicon, crawler policy, CNAME, and security contact are durable inputs.`, '', '| Public file | Purpose |', '|---|---|'];
  for (const file of publicFiles) {
    let purpose = 'Public article or site asset.';
    if (/\.(?:jpg|jpeg|png|svg|webp)$/i.test(file)) purpose = 'Article, social, diagram, or brand image.';
    if (/\/(?:rss|atom)\.xml$|public\/(?:rss|atom)\.xml$/.test(file)) purpose = 'Generated feed discovery surface.';
    if (file === 'public/feed.json') purpose = 'Generated JSON Feed surface.';
    if (file === 'public/image-sitemap.xml') purpose = 'Generated image-search discovery surface.';
    if (file === 'public/discovery-manifest.json') purpose = 'Generated deterministic discovery manifest.';
    if (/llms(?:-full)?\.txt$/.test(file)) purpose = 'AI-readable content index.';
    if (file === 'public/robots.txt') purpose = 'Search and AI crawler policy.';
    if (file === 'public/.well-known/security.txt') purpose = 'Responsible disclosure contact policy.';
    if (file === 'public/CNAME') purpose = 'GitHub Pages custom-domain contract.';
    discovery.push(`| ${repositoryLink(file)} | ${purpose} |`);
  }
  outputs.set('discovery-assets.md', `${discovery.join('\n')}\n`);

  const fingerprintFiles = [...new Set([
    ...sourceFiles,
    ...posts.map((post) => post.file),
    ...workflows.map((workflow) => workflow.file),
    ...socialPlans,
    ...videoPlans,
    'package.json', '.env.example', 'index.md', 'tags.md', 'POSTING.md', 'CONTRIBUTING.md',
    'video/templates/catalog.json', 'video/assets/stock-sources.json',
    'public/robots.txt', 'public/llms.txt', 'public/llms-full.txt', 'public/.well-known/security.txt', 'public/CNAME',
  ])].filter((file) => files.includes(file));
  const fingerprint = createHash('sha256');
  for (const file of fingerprintFiles.sort()) fingerprint.update(`${file}\0${read(file)}\0`);

  const explicitFocus = posts.filter((post) => matter(read(post.file)).data.focusKeyword).length;
  const readme = [marker, '', '# Flyto2 Blog Source And Content Reference', '', 'This reference is generated from repository source with the TypeScript compiler AST and from article frontmatter with `gray-matter`. It maps executable declarations, posts, automation, distribution plans, and public assets back to maintained files.', '', `Inventory: **${declarations.length} declarations**, **${sourceFiles.length} source files**, **${posts.length} published posts**, **${explicitFocus} explicit focus keywords**, **${Object.keys(packageJson.scripts ?? {}).length} npm scripts**, **${workflows.length} workflows**, **${environment.length} environment inputs**, **${socialPlans.length} social ${socialPlans.length === 1 ? 'plan' : 'plans'}**, **${videoPlans.length} video ${videoPlans.length === 1 ? 'plan' : 'plans'}**, and **${publicFiles.length} public files**.`, '', `Source fingerprint: \`${fingerprint.digest('hex')}\`.`, '', '## Source Areas', '', '| Area | Declarations | Reference |', '|---|---:|---|'];
  for (const page of sourcePages) readme.push(`| ${titleCase(page.area)} | ${page.declarations} | [${page.file}](${page.file}) |`);
  readme.push('', '## Public Contracts', '', ...postChunks.map((_, index) => `- [Editorial content ${index + 1}](posts-${String(index + 1).padStart(2, '0')}.md)`), '- [Automation and configuration](automation.md)', '- [Social and video distribution](distribution.md)', '- [Public discovery and assets](discovery-assets.md)');
  outputs.set('README.md', `${readme.join('\n')}\n`);
  return outputs;
}

function main() {
  const outputs = buildOutputs(repositoryFiles());
  if (checkMode) {
    const errors = [];
    const existing = existsSync(outputDir) ? readdirSync(outputDir).filter((file) => file.endsWith('.md')).sort() : [];
    for (const [file, content] of outputs) {
      const destination = path.join(outputDir, file);
      if (!existsSync(destination)) errors.push(`missing generated reference: docs/reference/${file}`);
      else if (readFileSync(destination, 'utf8') !== content) errors.push(`stale generated reference: docs/reference/${file}`);
    }
    for (const file of existing) if (!outputs.has(file)) errors.push(`obsolete generated reference: docs/reference/${file}`);
    if (errors.length) {
      for (const error of errors) console.error(error);
      process.exit(1);
    }
    console.log(`blog documentation reference is current: ${outputs.size} pages`);
    return;
  }

  rmSync(outputDir, { recursive: true, force: true });
  mkdirSync(outputDir, { recursive: true });
  for (const [file, content] of outputs) writeFileSync(path.join(outputDir, file), content);
  console.log(`blog documentation reference generated: ${outputs.size} pages`);
}

main();
